month=input("enter the month :")
if month=="february" or month=="march" or month=="april" or month=="may" :
    print(month," :summer season") 
elif month=="june" or month=="july" or month=="august" or month=="september" :
    print(month," :rainy season")
elif month=="october" or month=="november" or month=="desember" or month=="january":
    print( month," :winter season")
else:
    print("no")